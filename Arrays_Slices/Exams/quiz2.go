// 2. **Array Length:**
//    - Declare an array named `fruits` containing 5 strings representing different fruits.
//    - Print the length of the `fruits` array.

package main

import "fmt"

func main() {
	fruits := [5]string{"Mangoe", "Banana", "Apple", "Orange", "Lemon"}

	fmt.Printf("The Len is %v\n ", len(fruits))
}
