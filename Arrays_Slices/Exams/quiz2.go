// 2. **Array Length:**
//    - Declare an array named `fruits` containing 5 strings representing different fruits.
//    - Print the length of the `fruits` array.

package main

import "fmt"

func main() {
	fruits := [5]string{"Banana", "Orange", "Tomato", "Mangoe", "Lemon"}

	fmt.Print(len(fruits))
	fmt.Print("\n")

}
